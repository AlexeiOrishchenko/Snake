package com.game.snake.objects;

/**
 * @author Koliadin Nikita
 * @version 1.1
 *<p>This class is the mouse</p>
 */
public class Mouse {
    private int x;
    private int y;

    public Mouse(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
